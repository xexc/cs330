void SceneManager::CreateGreyCubeMesh()
{
	// Vertex data for a cube
	float vertices[] = {
		// positions          
		-1.0f, -1.0f, -1.0f,
		 1.0f, -1.0f, -1.0f,
		 1.0f,  1.0f, -1.0f,
		-1.0f,  1.0f, -1.0f,
		-1.0f, -1.0f,  1.0f,
		 1.0f, -1.0f,  1.0f,
		 1.0f,  1.0f,  1.0f,
		-1.0f,  1.0f,  1.0f
	};

	unsigned int indices[] = {
		0, 1, 2,
		2, 3, 0,
		4, 5, 6,
		6, 7, 4,
		4, 5, 1,
		1, 0, 4,
		7, 6, 2,
		2, 3, 7,
		5, 4, 7,
		7, 6, 5,
		0, 3, 2,
		2, 1, 0
	};

	glGenVertexArrays(1, &m_cubeVAO);
	glGenBuffers(1, &m_cubeVBO);
	glGenBuffers(1, &m_cubeEBO);

	glBindVertexArray(m_cubeVAO);

	glBindBuffer(GL_ARRAY_BUFFER, m_cubeVBO);
	glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STATIC_DRAW);

	glBindBuffer(GL_ELEMENT_ARRAY_BUFFER, m_cubeEBO);
	glBufferData(GL_ELEMENT_ARRAY_BUFFER, sizeof(indices), indices, GL_STATIC_DRAW);

	// position attribute
	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)0);
	glEnableVertexAttribArray(0);

	glBindBuffer(GL_ARRAY_BUFFER, 0);
	glBindVertexArray(0);
}
/***********************************************************
 *  PrepareScene()
 *
 *  This method is used for preparing the 3D scene by loading
 *  the shapes, textures in memory to support the 3D scene 
 *  rendering
 ***********************************************************/
void SceneManager::PrepareScene()
{
	SceneManager::CreateGreyCubeMesh();

	// Define light sources
	Light light1;
	light1.position = glm::vec3(0.0f, 5.0f, 0.0f);
	light1.ambient = glm::vec3(0.2f, 0.2f, 0.2f);
	light1.diffuse = glm::vec3(0.5f, 0.5f, 0.5f);
	light1.specular = glm::vec3(1.0f, 1.0f, 1.0f);

	// Pass light source to shader
	m_pShaderManager->use();
	m_pShaderManager->setVec3Value("light.position", light1.position);
	m_pShaderManager->setVec3Value("light.ambient", light1.ambient);
	m_pShaderManager->setVec3Value("light.diffuse", light1.diffuse);
	m_pShaderManager->setVec3Value("light.specular", light1.specular);

	// Define material properties
	Material material;
	material.ambient = glm::vec3(1.0f, 0.5f, 0.31f);
	material.diffuse = glm::vec3(1.0f, 0.5f, 0.31f);
	material.specular = glm::vec3(0.5f, 0.5f, 0.5f);
	material.shininess = 32.0f;

	// Pass material properties to shader
	m_pShaderManager->setVec3Value("material.ambient", material.ambient);
	m_pShaderManager->setVec3Value("material.diffuse", material.diffuse);
	m_pShaderManager->setVec3Value("material.specular", material.specular);
	m_pShaderManager->setFloatValue("material.shininess", material.shininess);

	if (CreateGLTexture("cup.jpg", "cup")) {
		std::cout << "good" << std::endl;
	}
	else
	{
		std::cout << "not good" << std::endl;
	}

	if (CreateGLTexture("wood.jpg", "wood")) {
		std::cout << "good" << std::endl;
	}
	else
	{
		std::cout << "not good" << std::endl;
	}
	if (CreateGLTexture("wood.jpg", "wood")) {
		std::cout << "good" << std::endl;
	}
	else
	{
		std::cout << "not good" << std::endl;
	}
	if (CreateGLTexture("pencil.jpg", "pencil")) {
		std::cout << "good" << std::endl;
	}
	else
	{
		std::cout << "not good" << std::endl;
	}
	BindGLTextures();
	// Load necessary meshes
	m_cupMesh = new ShapeMeshes();
	m_pencilMesh = new ShapeMeshes();

	// Load cup (cylinder) and pencils (boxes)
	m_cupMesh->LoadCylinderMesh();
	m_pencilMesh->LoadBoxMesh();

	// Set initial positions
	m_cupPosition = glm::vec3(0.0f, 0.0f, 0.0f);
	m_pencilPositions[0] = glm::vec3(-1.0f, 0.0f, 0.0f);
	m_pencilPositions[1] = glm::vec3(0.0f, 1.0f, 0.0f);
	m_pencilPositions[2] = glm::vec3(1.0f, 0.0f, 0.0f);
}

/***********************************************************
 *  RenderScene()
 *
 *  This method is used for rendering the 3D scene by 
 *  transforming and drawing the basic 3D shapes
 ***********************************************************/
void SceneManager::RenderScene()
{

	// Clear the screen to grey
	glClearColor(0.5f, 0.5f, 0.5f, 1.0f);
	glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

	// Bind the shader
	m_pShaderManager->use();

	// Draw cup
	SetShaderTexture("cup");
	glm::mat4 cupModel = glm::translate(glm::mat4(1.0f), m_cupPosition);
	m_pShaderManager->setMat4Value("model", cupModel);

	m_cupMesh->DrawCylinderMesh();

	// Draw pencils
	for (int i = 0; i < 3; ++i)
	{
		glm::mat4 pencilModel = glm::translate(glm::mat4(1.0f), m_pencilPositions[i]);
		m_pShaderManager->setMat4Value("model", pencilModel);
		
		if (i == 0)
			SetShaderTexture("wood");
		else if (i == 1)
			SetShaderTexture("pencil");
		else if (i == 2)
			SetShaderTexture("wood");

		m_pencilMesh->DrawBoxMesh();
	}
}
